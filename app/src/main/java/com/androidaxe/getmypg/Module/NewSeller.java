package com.androidaxe.getmypg.Module;

public class NewSeller {
    String email, id;

    public NewSeller(){}

    public NewSeller(String email, String id) {
        this.email = email;
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
